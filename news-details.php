                            <p>The TRCN Multipurpose Cooperative Society elections are coming up on the 18th of July 2025</p>
                            <p>
                                <strong>1.0 Introduction:</strong> The election guidelines are designed to enable contestants and voters participate 
                                appropriately in order to conduct credible, free and fair election. In line with the provisions of 
                                the TRCN Multipurpose Cooperative Byelaws, section 59 (c), the followings are presented as guidelines 
                                for the conduct of the 2025 general election.
                            </p>
                            <p>
                                <strong>2.0 Presentation of the guidelines: </strong>The guidelines are rooted in the society's byelaws section 59, 
                                (d), (e) with additional rules by the Independent Electoral Committee as follows:

                                <ol>
                                    <li>1. Candidates must be a member of the society for at least two (2) years.</li>
                                    <li>2. He/She must have attended at least 6 out of 8 meetings of the society.</li>
                                    <li>3. He/She must have 10% of Gross Salary as take home.</li>
                                    <li>4. Any member of Advisory Board who intends to contest election shall resign his or her appointment in writing to the Chairman.</li>
                                    <li>5. For all positions relating to finance, the candidate (s) shall be an Accountant and the assistant must have a related background.</li>
                                    <li>6. The election shall feature Open-Secret Ballot system</li>
                                    <li>7. Every member shall be eligible to vote once accredited.</li>
                                    <li>8. In the general conduct of the election, voters are to join line for accreditation after which they shall be given voting card for each position to be ticked appropriately.</li>
                                    <li>9. There shall be no open campaign at the voting venue.</li>
                                    <li>10. Individual voters are expected to cast their votes and leave the voting venue immediately to avoid confusion.</li>
                                    <li>11. Contestants would be required to present one (1) Agent each to monitor the election.</li>
                                    <li>12. Violators of the rules would be penalized.</li>
                                </ol>
                            </p>
